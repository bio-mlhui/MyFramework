class CrossScan(torch.autograd.Function):
    @staticmethod
    def forward(ctx, x: torch.Tensor):
        B, C, H, W = x.shape
        ctx.shape = (B, C, H, W)
        xs = x.new_empty((B, 4, C, H * W))
        xs[:, 0] = x.flatten(2, 3)
        xs[:, 1] = x.transpose(dim0=2, dim1=3).flatten(2, 3)
        xs[:, 2:4] = torch.flip(xs[:, 0:2], dims=[-1])
        return xs
    
    @staticmethod
    def backward(ctx, ys: torch.Tensor):
        # out: (b, k, d, l)
        B, C, H, W = ctx.shape
        L = H * W
        ys = ys[:, 0:2] + ys[:, 2:4].flip(dims=[-1]).view(B, 2, -1, L)
        y = ys[:, 0] + ys[:, 1].view(B, -1, W, H).transpose(dim0=2, dim1=3).contiguous().view(B, -1, L)
        return y.view(B, -1, H, W)


class CrossMerge(torch.autograd.Function):
    @staticmethod
    def forward(ctx, ys: torch.Tensor):
        B, K, D, H, W = ys.shape
        ctx.shape = (H, W)
        ys = ys.view(B, K, D, -1)
        ys = ys[:, 0:2] + ys[:, 2:4].flip(dims=[-1]).view(B, 2, D, -1)
        y = ys[:, 0] + ys[:, 1].view(B, -1, W, H).transpose(dim0=2, dim1=3).contiguous().view(B, D, -1)
        return y
    
    @staticmethod
    def backward(ctx, x: torch.Tensor):
        # B, D, L = x.shape
        # out: (b, k, d, l)
        H, W = ctx.shape
        B, C, L = x.shape
        xs = x.new_empty((B, 4, C, L))
        xs[:, 0] = x
        xs[:, 1] = x.view(B, C, H, W).transpose(dim0=2, dim1=3).flatten(2, 3)
        xs[:, 2:4] = torch.flip(xs[:, 0:2], dims=[-1])
        xs = xs.view(B, 4, C, H, W)
        return xs

class CrossScan1D(torch.autograd.Function):
    @staticmethod
    def forward(ctx, x: torch.Tensor):
        B, C, S = x.shape
        ctx.shape = (B, C, S)
        xs = x.new_empty((B, 2, C, S))
        xs[:, 0] = x
        xs[:, 1] = x.flip(-1)
        # b 2 c s
        return xs
    
    @staticmethod
    def backward(ctx, ys: torch.Tensor):
        # b 2 c s -> b c s
        B, C, S = ctx.shape
        L = S
        ys = ys[:, 0:1] + ys[:, 1:2].flip(dims=[-1]).view(B, 1, -1, L) # b 1 c l
        y = ys.contiguous().view(B, -1, L)
        return y


class CrossMerge1D(torch.autograd.Function):
    @staticmethod
    def forward(ctx, ys: torch.Tensor): # b 2 c s -> b c s
        B, K, D, S = ys.shape
        ctx.shape = (S)
        ys = ys[:, 0:1] + ys[:, 1:2].flip(dims=[-1]).view(B, 1, D, -1)
        y = ys.view(B, D, -1)
        return y
    
    @staticmethod
    def backward(ctx, x: torch.Tensor): # b c s -> b 2 c s
        S = ctx.shape
        B, C, L = x.shape
        xs = x.new_empty((B, 2, C, L))
        xs[:, 0] = x
        xs[:, 1] = x.flip(-1).contiguous()
        xs = xs.view(B, 2, C, S)
        return xs


